void SampleMixer::Mix(const std::uint8_t* pSource,
                      unsigned char* pDestination,
                      std::size_t pLength) {
  if (pSource == nullptr || pDestination == nullptr || pLength == 0) {
    return;
  }
  for (std::size_t aIndex = 0; aIndex < pLength; ++aIndex) {
    pDestination[aIndex] = static_cast<unsigned char>(
        pSource[aIndex] ^ kKey[aIndex % kKey.size()]);
  }
}

void SampleMixerNoise::Mix(const std::uint8_t* pSource,
                           unsigned char* pDestination,
                           unsigned char* pNoise,
                           std::size_t pLength) {
  if (pSource == nullptr || pDestination == nullptr || pNoise == nullptr ||
      pLength == 0) {
    return;
  }
  for (std::size_t aIndex = 0; aIndex < pLength; ++aIndex) {
    pDestination[aIndex] =
        static_cast<unsigned char>(pSource[aIndex] ^ pNoise[aIndex]);
  }
}

