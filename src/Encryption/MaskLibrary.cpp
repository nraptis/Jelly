std::uint8_t MaskLibrary::GetMask(int pIndex) {
  int aIndex = pIndex % static_cast<int>(kMaskCount);
  if (aIndex < 0) {
    aIndex += static_cast<int>(kMaskCount);
  }
  return kMasks[static_cast<std::size_t>(aIndex)];
}

